SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT
	  dd.BusinessDateID
	, COUNT(L.LoanNumber) 'Folders'

FROM QLODS.dbo.LKWD L
	INNER JOIN QLODS.dbo.DateDim dd ON dd.DateID = L.[1stStat33ID]

WHERE 1=1
	--AND L.Stat21ID BETWEEN 20170700 AND 20170999
	AND L.[1stStat33ID] BETWEEN 20170700 AND 20170999
	AND L.LoanPurposeID = 7

GROUP BY dd.BusinessDateID