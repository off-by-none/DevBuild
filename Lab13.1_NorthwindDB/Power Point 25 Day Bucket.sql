SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

DROP TABLE IF EXISTS #bp
SELECT 
  L.LoanNumber
, L.Stat20Dt
, L.Stat43Dt
, [20 to 43 TT]		= DATEDIFF(second, L.Stat20Dt, L.Stat43Dt)/86400.0
, [25 day bucket]	= CASE WHEN DATEDIFF(second, L.Stat20Dt, L.Stat43Dt)/86400.0 <= 25 THEN 1 ELSE 0 END
INTO #bp
FROM QLODS.dbo.LKWD L WITH (NOLOCK)
WHERE 1=1
	AND L.DELETEFLG = 0
	AND L.ReverseFlg = 0
	AND L.ClosingID BETWEEN 20191001 AND 20191031


SELECT AVG(#bp.[25 day bucket]*1.0) FROM #bp